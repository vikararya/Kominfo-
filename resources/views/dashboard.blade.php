    <script>
        // Data dari backend
        const months = @json($months);
        const monthlyViews = @json($monthlyViews);
        const coverLabels = @json($coverLabels);
        const coverViews = @json($coverViews);
        const coverColors = @json($coverColors);
    
        // Set new default font family and font color to mimic Bootstrap's default styling
        Chart.defaults.global.defaultFontFamily = 'Nunito', '-apple-system,system-ui,BlinkMacSystemFont,"Segoe UI",Roboto,"Helvetica Neue",Arial,sans-serif';
        Chart.defaults.global.defaultFontColor = '#858796';
    
        // Format angka
        function number_format(number, decimals, dec_point, thousands_sep) {
            number = (number + '').replace(',', '').replace(' ', '');
            var n = !isFinite(+number) ? 0 : +number,
                prec = !isFinite(+decimals) ? 0 : Math.abs(decimals),
                sep = (typeof thousands_sep === 'undefined') ? ',' : thousands_sep,
                dec = (typeof dec_point === 'undefined') ? '.' : dec_point,
                s = '',
                toFixedFix = function(n, prec) {
                    var k = Math.pow(10, prec);
                    return '' + Math.round(n * k) / k;
                };
            s = (prec ? toFixedFix(n, prec) : '' + Math.round(n)).split('.');
            if (s[0].length > 3) {
                s[0] = s[0].replace(/\B(?=(?:\d{3})+(?!\d))/g, sep);
            }
            if ((s[1] || '').length < prec) {
                s[1] = s[1] || '';
                s[1] += new Array(prec - s[1].length + 1).join('0');
            }
            return s.join(dec);
        }
    
        // ================================
        // Bar Chart
        // ================================
        const ctx = document.getElementById("myBarChart").getContext("2d");
        let myBarChart;
    
        function renderBarChart(startIndex, monthCount) {
            const endIndex = Math.min(startIndex + monthCount, months.length);
            const chartLabels = months.slice(startIndex, endIndex);
            const chartData = monthlyViews.slice(startIndex, endIndex);
    
            if (myBarChart) {
                myBarChart.destroy();
            }
    
            myBarChart = new Chart(ctx, {
                type: 'bar',
                data: {
                    labels: chartLabels,
                    datasets: [{
                        label: "Total Pengunjung",
                        backgroundColor: "#4e73df",
                        hoverBackgroundColor: "#2e59d9",
                        borderColor: "#4e73df",
                        data: chartData,
                    }],
                },
                options: {
                    maintainAspectRatio: false,
                    layout: { padding: { left: 10, right: 25, top: 25, bottom: 0 } },
                    scales: {
                        xAxes: [{
                            gridLines: { display: false, drawBorder: false },
                            ticks: { maxTicksLimit: 6 },
                            maxBarThickness: 25,
                        }],
                        yAxes: [{
                            ticks: {
                                beginAtZero: true,
                                padding: 10,
                                callback: function(value) {
                                    return number_format(value);
                                }
                            },
                            gridLines: {
                                color: "rgb(234, 236, 244)",
                                zeroLineColor: "rgb(234, 236, 244)",
                                drawBorder: false,
                                borderDash: [2],
                                zeroLineBorderDash: [2]
                            }
                        }],
                    },
                    legend: { display: false },
                    tooltips: {
                        titleMarginBottom: 10,
                        titleFontColor: '#6e707e',
                        titleFontSize: 14,
                        backgroundColor: "rgb(255,255,255)",
                        bodyFontColor: "#858796",
                        borderColor: '#dddfeb',
                        borderWidth: 1,
                        xPadding: 15,
                        yPadding: 15,
                        displayColors: false,
                        caretPadding: 10,
                        callbacks: {
                            label: function(tooltipItem, chart) {
                                return chart.datasets[tooltipItem.datasetIndex].label + ': ' + number_format(tooltipItem.yLabel);
                            }
                        }
                    },
                }
            });
        }
    
        // ================================
        // Pie Chart (Top 5 Most Viewed)
        // ================================
        if (coverLabels.length > 0) {
            const ctx2 = document.getElementById("myPieChart").getContext("2d");
    
            const darkenColor = (hex, amount) => {
                return '#' + hex.replace(/^#/, '').replace(/../g, (color) => {
                    const c = Math.max(0, parseInt(color, 16) - amount);
                    return c.toString(16).padStart(2, '0');
                });
            };
    
            const pieChart = new Chart(ctx2, {
                type: 'doughnut',
                data: {
                    labels: coverLabels,
                    datasets: [{
                        data: coverViews,
                        backgroundColor: coverColors,
                        hoverBackgroundColor: coverColors.map(color => darkenColor(color, 30)),
                        hoverBorderColor: "rgba(234, 236, 244, 1)",
                    }],
                },
                options: {
                    maintainAspectRatio: false,
                    tooltips: {
                        backgroundColor: "rgb(255,255,255)",
                        bodyFontColor: "#858796",
                        borderColor: '#dddfeb',
                        borderWidth: 1,
                        xPadding: 15,
                        yPadding: 15,
                        displayColors: false,
                        caretPadding: 10,
                        callbacks: {
                            label: function(tooltipItem, data) {
                                const label = data.labels[tooltipItem.index] || '';
                                const value = data.datasets[0].data[tooltipItem.index];
                                const total = data.datasets[0].data.reduce((a, b) => a + b, 0);
                                const percentage = Math.round((value / total) * 100);
                                return `${label}: ${number_format(value)} views (${percentage}%)`;
                            }
                        }
                    },
                    legend: { display: false },
                    cutoutPercentage: 80,
                }
            });
    
            // Create custom legend
            const legendContainer = document.getElementById('pieChartLegend');
            legendContainer.innerHTML = coverLabels.map((label, index) =>
                `<span class="mr-2"><i class="fas fa-circle" style="color:${coverColors[index]}"></i> ${label}</span>`
            ).join('');
        }
    
        // ================================
        // Initial Load + Toggle Buttons
        // ================================
        renderBarChart(0, 6); // Tampilkan bulan Jan–Jun awal
    
        document.getElementById("firstHalfBtn").addEventListener("click", function() {
            renderBarChart(0, 6);
        });
    
        document.getElementById("secondHalfBtn").addEventListener("click", function() {
            renderBarChart(6, 6);
        });
    
        // Warning jika data bulan tidak lengkap
        if (months.length !== 12 || monthlyViews.length !== 12) {
            console.warn('Data bulan atau views tidak lengkap (harus 12 bulan)');
        }
    </script>
    
